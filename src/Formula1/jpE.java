    private void jAccionEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jAccionEmpresaActionPerformed
     
     if((!Formula1UI.inem.empresas.isEmpty()) && (!bIden.getText().equals(""))){ // Si existen Empresas
        int x= Integer.parseInt(bIden.getText())-1;
//Alta Oferta
        if (bAltaOferta.isSelected()) {
            jpEAlta pEalta = new jpEAlta();
            if(JOptionPane.showConfirmDialog(this, pEalta, "Oferta",
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE)
            == JOptionPane.OK_OPTION) {                                
            Formula1UI.inem.empresas.get(x).altaOferta(pEalta.getDescripcion(),
            pEalta.getEstudios(),pEalta.getContrato(),pEalta.getSalario());
            bAviso.setText ("La Oferta se ha insertado satisfactoriamente");
              }
//Baja Oferta
        } else if (bBajaOferta.isSelected()){
          if(Formula1UI.inem.empresas.get(x).getNumOfertas() != 0){
            jpEBaja pEbaja = new jpEBaja(x);
            if(JOptionPane.showConfirmDialog(this, pEbaja, "Ofertas",
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE)
            == JOptionPane.OK_OPTION) {               
            OfertaDeEmpleo oferta= Formula1UI.inem.empresas.get(x).lisOfertas.get(pEbaja.getNumero()-1);
            Formula1UI.inem.empresas.get(x).bajaOferta(oferta);
            bAviso.setText ("La Oferta se ha borrado satisfactoriamente");
            }
          }else{
               bAviso.setText("No tiene Ofertas");
          }
// Consultar Oferta
        } else if (bConsultaOferta.isSelected()){            
            if(Formula1UI.inem.empresas.get(x).getNumOfertas() != 0){
                jpEConsulta pEconsulta = new jpEConsulta();
             if(JOptionPane.showConfirmDialog(this, pEconsulta, "Ofertas",
             JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE)
             == JOptionPane.OK_OPTION) {                 
                int salario = pEconsulta.getSalario();
                StringBuilder s= new StringBuilder();
                for (OfertaDeEmpleo of: Formula1UI.inem.empresas.get(x).lisOfertas){
                    if (of.getRangoSalarial()== salario){
                       s.append(Formula1UI.inem.empresas.get(x).
                       consultaOferta(of).toString());
                    }
                }
                listaempresas.setText(s.toString());
                }
            }else{
               bAviso.setText("No tiene Ofertas");              
            }
//Modificar Oferta
        } else if (bModificacionOferta.isSelected()){
            if(Formula1UI.inem.empresas.get(x).getNumOfertas() != 0){
                jpEModifica pEmodif = new jpEModifica(x);
            if(JOptionPane.showConfirmDialog(this, pEmodif, "Empresa",
            JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE)
            == JOptionPane.OK_OPTION) {
               Formula1UI.inem.empresas.get(x).modificacionOferta(
               pEmodif.getNumero(),pEmodif.getDescripcion(),
               pEmodif.getEstudios(),pEmodif.getContrato(),pEmodif.getSalario());
               bAviso.setText("Modificado con Exito");
            }
            }else{
               bAviso.setText("No tiene Ofertas");
            }
        }
     }else{                                        // Si no existen Empresas
        bAviso.setText("No hay Empresas o no ha introducido un n√∫mero");
     }
}//GEN-LAST:event_jAccionEmpresaActionPerformed

