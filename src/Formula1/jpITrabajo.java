public Date getAlta() throws ParseException{
    SimpleDateFormat a = new SimpleDateFormat("dd/MM/yyyy");
    return a.parse(falta.getText());
}

public Date getBaja() throws ParseException{
    SimpleDateFormat a = new SimpleDateFormat("dd/MM/yyyy");
    return a.parse(fbaja.getText());
}

public String getEmpresa(){
    String s= "Sin Empresa";
    if (!empre.getText().equals("")){
        return empre.getText();
    }else{
        return s;
    }
}

public String getPuesto(){
    String s= "Sin Puesto";
    if (!puest.getText().equals("")){
        return puest.getText();
    }else{
        return s;
    }  
}

public boolean getSi(){
    boolean s=false;
    if (bsi.isSelected()){
        s=true;
    }
    return s;
}

